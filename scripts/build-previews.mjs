#!/usr/bin/env node
// Copies registry component source into web/components/previews/ and generates
// a next/dynamic index map so the site can render live previews.  Run
// automatically via predev/prebuild.

import { mkdirSync, rmSync, writeFileSync, readFileSync } from "node:fs";
import { join } from "node:path";
import { readAllEntries, rootDir } from "./lib/read-registry.mjs";

const previewsDir = join(rootDir, "web", "components", "previews");

function main() {
  const entries = readAllEntries();

  rmSync(previewsDir, { recursive: true, force: true });
  mkdirSync(previewsDir, { recursive: true });

  const map = [];

  for (const entry of entries) {
    const src = join(rootDir, "registry", entry.template_id, "component.tsx");
    const baseName = entry.component_filename.replace(/\.tsx$/, "");
    const dest = join(previewsDir, entry.component_filename);

    writeFileSync(dest, readFileSync(src, "utf8"), "utf8");
    map.push({ id: entry.template_id, baseName });
  }

  const lines = [
    "// Auto-generated by scripts/build-previews.mjs — do not edit.",
    'import dynamic from "next/dynamic";',
    'import type { ComponentType } from "react";',
    "",
    "const previews: Record<string, ComponentType> = {",
    ...map.map(
      ({ id, baseName }) =>
        `  "${id}": dynamic(() => import("./${baseName}"), { ssr: false }),`,
    ),
    "};",
    "",
    "export default previews;",
    "",
  ];

  writeFileSync(join(previewsDir, "index.ts"), lines.join("\n"), "utf8");

  console.error(
    `build-previews: wrote ${entries.length} preview(s) + index.ts to web/components/previews/`,
  );
}

main();
